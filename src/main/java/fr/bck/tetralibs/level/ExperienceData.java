public class ExperienceData {
    private int level;
    private int experience;

    public ExperienceData(int level, int experience) {
        this.level = level;
        this.experience = experience;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

    public int getExperience() {
        return experience;
    }

    public void setExperience(int experience) {
        this.experience = experience;
    }

    public int getExperienceToNextLevel() {
        return 10 * level; // Exemple : 10 * niveau actuel.
    }

    public void levelUp() {
        this.level++;
    }
}
