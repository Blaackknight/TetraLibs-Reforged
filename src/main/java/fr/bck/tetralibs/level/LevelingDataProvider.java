public class LevelingDataProvider implements ICapabilityProvider, INBTSerializable<CompoundTag> {
    private final LazyOptional<ILevelingData> instance = LazyOptional.of(LevelingData::new);

    @Override
    public <T> @NotNull LazyOptional<T> getCapability(@NotNull Capability<T> cap, Direction side) {
        return cap == LevelingDataCapability.LEVELING_DATA_CAPABILITY ? instance.cast() : LazyOptional.empty();
    }

    @Override
    public CompoundTag serializeNBT() {
        return instance.orElseThrow(IllegalStateException::new).serializeNBT();
    }

    @Override
    public void deserializeNBT(CompoundTag nbt) {
        instance.orElseThrow(IllegalStateException::new).deserializeNBT(nbt);
    }
}
