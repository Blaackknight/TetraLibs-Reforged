@OnlyIn(Dist.CLIENT)
public final class ClientSoundHandler {
    public static void playLooping(PlayLoopingSoundPacket p) {
        ClientLevel lvl = Minecraft.getInstance().level;
        if (lvl == null) return;
        Entity e = lvl.getEntity(p.entityId);
        if (!(e instanceof Player pl)) return;

        SoundEvent se = ForgeRegistries.SOUND_EVENTS.getValue(p.sound);
        if (se == null) return;

        LoopingEntitySoundInstance inst = new LoopingEntitySoundInstance(pl, se, SoundSource.PLAYERS, p.volume, p.pitch);
        Minecraft.getInstance().getSoundManager().play(inst);
    }

    public static void stopLooping(StopLoopingSoundPacket p) {
        Minecraft.getInstance().getSoundManager().stop(p.sound, SoundSource.PLAYERS);
    }
}
