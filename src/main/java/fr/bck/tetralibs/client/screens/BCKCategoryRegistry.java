/**
 * Point central où les mods peuvent enregistrer leurs catégories.
 */
public final class BCKCategoryRegistry {
    private static final List<BCKCategory> CATEGORIES = new ArrayList<>();

    public static void register(BCKCategory cat) {
        if (!CATEGORIES.contains(cat)) CATEGORIES.add(cat);
    }

    public static List<BCKCategory> getAll() {
        return Collections.unmodifiableList(CATEGORIES);
    }

    public static void clear() { CATEGORIES.clear(); }

    private BCKCategoryRegistry() {}
}
