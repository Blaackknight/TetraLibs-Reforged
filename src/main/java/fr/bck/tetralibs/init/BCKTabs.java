@Mod.EventBusSubscriber(bus = Mod.EventBusSubscriber.Bus.MOD)
public class BCKTabs {
    public static final DeferredRegister<CreativeModeTab> REGISTRY = DeferredRegister.create(Registries.CREATIVE_MODE_TAB, TetralibsMod.MODID);

    @SubscribeEvent
    public static void buildTabContentsVanilla(BuildCreativeModeTabContentsEvent tabData) {
        if (tabData.getTabKey() == CreativeModeTabs.TOOLS_AND_UTILITIES) {
            tabData.accept(BCKItems.BCK_STAFF.get());
        } else if (tabData.getTabKey() == CreativeModeTabs.OP_BLOCKS) {
            if (tabData.hasPermissions()) {
                tabData.accept(BCKItems.BCK_STAFF.get());
            }
        }
    }

}
