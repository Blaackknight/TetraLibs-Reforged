public class BCKBackpackScreen extends AbstractContainerScreen<BCKBackpackMenu> {

    protected static final ResourceLocation TEX = ResourceLocation.fromNamespaceAndPath("minecraft", "textures/gui/container/generic_54.png");

    private final int rows;

    public BCKBackpackScreen(BCKBackpackMenu menu, Inventory inv, Component title) {
        super(menu, inv, title);
        this.rows = menu.getRows();
        this.imageHeight = 114 + rows * 18;
        this.inventoryLabelY = this.imageHeight - 94;
    }

    @Override
    protected void renderBg(GuiGraphics gg, float partial, int mx, int my) {
        gg.blit(TEX, leftPos, topPos, 0, 0, 176, 17 + rows * 18);
        gg.blit(TEX, leftPos, topPos + 17 + rows * 18, 0, 126, 176, 96);
    }
}
