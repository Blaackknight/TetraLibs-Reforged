public final class BCKBackpack {

    public final String id;
    public final int slots;
    public final RegistryObject<Item> item;
    public final RegistryObject<MenuType<BCKBackpackMenu>> menu;
    public final BCKBackpackRegistry.ScreenFactory screenFactory;

    BCKBackpack(String id, int slots, RegistryObject<Item> item, RegistryObject<MenuType<BCKBackpackMenu>> menu, BCKBackpackRegistry.ScreenFactory screenFactory) {

        this.id = id;
        this.slots = slots;
        this.item = item;
        this.menu = menu;
        this.screenFactory = screenFactory;
    }

    public ResourceLocation rl() {
        return ResourceLocation.fromNamespaceAndPath(BCKBackpackRegistry.MODID, id);
    }
}
