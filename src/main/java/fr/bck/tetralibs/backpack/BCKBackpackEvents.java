public final class BCKBackpackEvents {

    /**
     * Ajoute la capability si l'item correspond à un tier enregistré
     */
    @SubscribeEvent
    public static void onAttachCaps(AttachCapabilitiesEvent<ItemStack> e) {
        ItemStack stack = e.getObject();
        int size = BCKBackpackRegistry.getSlots(stack);   // ← dynamique
        if (size > 0) {
            e.addCapability(BCKBackpackInvProvider.ID,   // modid = tetra_libs
                    new BCKBackpackInvProvider(stack, size));
        }
    }
}
