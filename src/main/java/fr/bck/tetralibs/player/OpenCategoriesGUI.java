/**
 * Point d’entrée unique pour afficher le gestionnaire de catégories.
 */
@OnlyIn(Dist.CLIENT)
public final class OpenCategoriesGUI {

    /**
     * Appelée uniquement par S2COpenCategoriesGui.handle(...)
     */
    static void openLocally() {          //  ← plus « private »
        Minecraft mc = Minecraft.getInstance();
        if (mc.player == null) return;
        mc.execute(() -> mc.setScreen(new BCKManagementScreen()));
    }

    public static void requestOpen() {
        TetralibsMod.PACKET_HANDLER.sendToServer(new C2SRequestCategoriesGui());
    }

    private OpenCategoriesGUI() {
    }
}
