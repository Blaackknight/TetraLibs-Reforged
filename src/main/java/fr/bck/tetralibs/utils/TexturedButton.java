public class TexturedButton extends AbstractWidget {
    private final ResourceLocation idle, hover, down;
    private final Runnable onClick;

    public TexturedButton(int x, int y, int w, int h, Component txt, ResourceLocation idle, ResourceLocation hover, ResourceLocation down, Runnable run) {
        super(x, y, w, h, txt);
        this.idle = idle;
        this.hover = hover;
        this.down = down;
        this.onClick = run;
    }

    @Override
    public void renderWidget(GuiGraphics g, int mx, int my, float d) {
        if (!visible) return;
        ResourceLocation tex = !active ? idle : isMouseOver(mx, my) ? (isFocused() ? down : hover) : idle;
        g.blit(tex, getX(), getY(), 0, 0, width, height, width, height);
        int col = active ? 0xFFFFFF : 0x777777;
        g.drawCenteredString(Minecraft.getInstance().font, getMessage(), getX() + width / 2, getY() + (height - 8) / 2, col);
    }

    @Override
    public void onClick(double mx, double my) {
        if (active) onClick.run();
    }

    @Override
    protected void updateWidgetNarration(@NotNull NarrationElementOutput o) {
    }
}
