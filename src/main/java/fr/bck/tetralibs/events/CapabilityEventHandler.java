public class CapabilityEventHandler {
    @SubscribeEvent
    public static void onAttachCapabilities(AttachCapabilitiesEvent<Entity> event) {
        // Attache la capability uniquement aux entités qui implémentent LevelingEntity
        if (event.getObject() instanceof LevelingEntity) {
            event.addCapability(ResourceLocation.fromNamespaceAndPath("tetra_libs", "leveling_data"), new LevelingDataProvider());
        }
    }
}